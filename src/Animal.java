public class Animal {
 String name;
 int age;
 String healthStatus;
 public Animal(String name, int age, String healthStatus) {
 this.name = name;
 this.age = age;
 this.healthStatus = healthStatus;
 }
 public String getName() {
    return name;
}

public int getAge() {
    return age;
}

public String getHealthStatus() {
    return healthStatus;
}

}
